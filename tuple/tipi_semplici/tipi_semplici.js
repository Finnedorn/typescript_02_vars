"use strict";
// in typescript le variabili sono di tipo esplicito
// (a differenza di js puro in cui dichiariamo le variabili in modo implicito)
// necessitano quindi di specificare che tipo di dato contengano
// ecco qualche esempio di tipi di variabili e costanti:
// string 
let saluto = 'ciao';
let nome = 'Daniele';
console.log(typeof nome && saluto);
// number
// bigInt
let intero = 8;
let decimale = 23.24;
console.log(typeof intero && decimale);
// boolean
let vero = true;
let falso = false;
console.log(typeof vero && falso);
